valor1 = 10
valor2 = 2

# Soma
print(valor1 + valor2)

# Subtracao
print(valor1 - valor2)

# Multiplicacao
print(valor1 * valor2)

# Divisao
print(valor1 / valor2)

# Divisao (// Floor Division remove o valor decimal da divisao)
print(valor1 // valor2)

# Divisao (% modulo que retorna o resto da divisao)
print(valor1 % valor2)

# Potenciacao
print(valor1 ** valor2)
